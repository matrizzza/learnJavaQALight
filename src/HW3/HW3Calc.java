package HW3;

public class HW3Calc {
    public int subtractionDoubled(int decrement, int subtractor){
        int result = (decrement - subtractor)*2;
        System.out.println("substractionDoubled result = " + result);
        return result;
    }

    public int multiplicationDoubled(int multiplier1, int multiplier2){
        int result = (multiplier1*multiplier2)*2;
        System.out.println("multiplicationDoubled result = " + result);
        return result;
    }
}
